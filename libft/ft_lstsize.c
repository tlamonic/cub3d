#include "libft.h"

int		ft_lstsize(t_list *lst)
{
	t_list	*list;
	int		i;

	i = 0;
	if (lst)
	{
		list = lst;
		while (list)
		{
			list = list->next;
			i++;
		}
	}
	return (i);
}
